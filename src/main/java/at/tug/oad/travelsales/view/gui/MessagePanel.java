	private void updateInformation() {
		if (messages == null) {
			return;
		}
		model = new DefaultListModel();
		liMessages.setModel(model);

		for (INotification m : messages) {
			String showString = "";
			if (m.getReadTime() == null) // message unread
			{
				showString += "*  ";
			} else {
				showString += "   ";
			}

			showString += "From: ";
			showString += m.getSender().getUserName();
			showString += " (Type: " + m.getNotificationType().toString() + ")";

			model.addElement(showString);
		}
	}

