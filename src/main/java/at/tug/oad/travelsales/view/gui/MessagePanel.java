	private void onListValueChanged(javax.swing.event.ListSelectionEvent evt) {// GEN-FIRST:event_onListValueChanged
		int index = liMessages.getSelectedIndex();
		if (index < 0) {
			taText.setText("");
			return;
		}
		INotification message = ((INotification[]) messages.toArray())[index];
		taText.setText(TravelSalesViewController.getInstance().readMessage(message));
	}// GEN-LAST:event_onListValueChanged

