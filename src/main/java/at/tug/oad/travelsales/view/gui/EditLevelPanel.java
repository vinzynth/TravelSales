	private void onListValueChanged(javax.swing.event.ListSelectionEvent evt) {// GEN-FIRST:event_onListValueChanged
		int index = liBaseGames.getSelectedIndex();
		if (index < 0) {
			return;
		}

		selectedBaseGame = level.getNestedCollection().get(index);
		taDescription.setText(selectedBaseGame.toString());
	}// GEN-LAST:event_onListValueChanged

	private void onAddNewBaseGame(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_onAddNewBaseGame
		IBaseGame newBaseGame = TravelSalesViewController.getInstance().newBaseGame("New BaseGame");
		TravelSalesViewController.getInstance().editLevel(level, l -> l.add(newBaseGame));
		TravelSalesViewController.getInstance().showMessageDialog("Success", "New Base-Game created!\nYou can edit Base-Games in the edit-screen.", MessageType.SUCCESS);

		updateInformation();
	}// GEN-LAST:event_onAddNewBaseGame

	private void onDeleteBaseGame(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_onDeleteBaseGame
		String question = selectedBaseGame.getName() + " will be deleted! Continue?";

		int a = JOptionPane.showConfirmDialog(this, question, "Please confirm", JOptionPane.YES_NO_OPTION);
		if (a == JOptionPane.YES_OPTION) {
			TravelSalesViewController.getInstance().editLevel(level, l -> l.remove(selectedBaseGame));
		}
	}// GEN-LAST:event_onDeleteBaseGame

	private void onSaveName(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_onSaveName
		TravelSalesViewController.getInstance().editLevel(level, l -> l.setName(tfName.getText()));
	}// GEN-LAST:event_onSaveName

	private void onBack(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_onBack
		TravelSalesViewController.getInstance().cancel();
	}// GEN-LAST:event_onBack

