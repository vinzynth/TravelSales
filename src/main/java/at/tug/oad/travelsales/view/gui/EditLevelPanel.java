	private void updateInformation() {
		if (level == null) {
			return;
		}
		SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
		tfName.setText(level.getName());
		tfCreator.setText(level.getCreator().getUserName());
		tfCreationDate.setText(sdf.format(level.getCreateDate()));
		model = new DefaultListModel();
		liBaseGames.setModel(model);
		for (IBaseGame bgame : level.getNestedCollection()) {
			model.addElement(bgame.getName());
		}

	}

